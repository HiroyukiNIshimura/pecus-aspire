# Coati 機能仕様書: タスク管理モジュール (v2.0)

最終更新: 2026-01-20
作成者: 開発チーム

## 1. 概要
本ドキュメントは、Coati プロジェクトにおける「タスク管理モジュール」の機能要件および仕様を定義するものです。本モジュールは、ユーザーがプロジェクト内の作業項目を作成・追跡・完了するための核心的な機能を提供します。

## 2. 機能要件

### 2.1 タスク作成
- ユーザーは任意のプロジェクト内でタスクを作成できる。
- 必須項目: タイトル (最大200文字)、担当者 (未設定可)。
- 任意項目: 説明、期限、優先度、タグ、添付ファイル。

### 2.2 ステータス遷移
タスクは以下のライフサイクルを持つ:
1. `Backlog` - 未着手
2. `ToDo` - 着手予定
3. `In Progress` - 作業中
4. `Review` - 確認待ち
5. `Done` - 完了
- ステータス変更時には、変更履歴 (Activity Log) が自動的に記録されること。

### 2.3 フィルタリングと検索
- 以下の条件でタスクをフィルタリング可能とする:
  - 担当者
  - ステータス
  - 期限 (今日、今週、期限切れ)
  - 全文検索 (タイトル・説明)

## 3. UI/UX 仕様

### 3.1 タスク詳細モーダル
- 画面遷移せず、モーダルダイアログまたはサイドパネルで詳細を表示する。
- 説明フィールドは Markdown 記法をサポートし、プレビュー機能を備える。
- コメント機能はリアルタイム更新 (SignalR) をサポートする。

### 3.2 カンバンボード表示
- ステータスごとにカラムを表示し、ドラッグ&ドロップでステータスを変更可能とする。
- レスポンシブ対応: モバイル端末ではリスト表示に切り替える。

## 4. データモデル要件

### Task エンティティ
| フィールド名 | 型 | 制約 | 説明 |
|---|---|---|---|
| Id | UUID | PK | 一意識別子 |
| Title | String | Max: 200 | タスク名 |
| Status | Enum | Not Null | 現在の状態 |
| AssigneeId | UUID | Nullable | 担当ユーザーID |
| DueDate | DateTime | Nullable | 完了期限 |

## 5. 非機能要件
- **パフォーマンス**: タスク一覧の初期表示は 500ms 以内であること (ページネーション適用)。
- **セキュリティ**: プロジェクトメンバー以外のアクセスを拒否すること (RBAC)。
- **整合性**: 複数ユーザーによる同時編集時は、楽観的排他制御 (Optimistic Concurrency) を用いて競合を検知する。

## 6. 未決定事項
- アーカイブ済みタスクの自動削除ポリシー
- サードパーティ製カレンダーツールとの連携仕様

---
*End of Document*
""";
