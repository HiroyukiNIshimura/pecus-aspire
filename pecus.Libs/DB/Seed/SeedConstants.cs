### 変更の同時発生（競合）を扱う流れ
1. User A がタスクの編集を開始すると、User B の画面には「User A が編集中」と表示され、編集ボタンが押せなくなる（無駄な衝突の予防）。
2. User A が編集を終えると、User B はすぐに編集可能になる。
3. 万が一、タイミングのズレなどで同時に保存された場合は、後から保存したユーザーに「競合」を通知し、上書きするかどうかを判断してもらう（最終防衛）。

期待される価値: 「せっかく書いたのに保存できなかった」という悲劇を未然に防ぐ。お互いの気配を感じながら、スムーズに連携できる。

