using System.ComponentModel.DataAnnotations;

namespace Pecus.Models.Responses.Workspace;

/// <summary>
/// ワークスペースユーザー一覧用DTO（軽量）
/// </summary>
public class WorkspaceUserItem
{
    /// <summary>
    /// ユーザーID
    /// </summary>
    [Required]
    public required int UserId { get; set; }

    /// <summary>
    /// ユーザー名
    /// </summary>
    [Required]
    public required string Username { get; set; }

    /// <summary>
    /// メールアドレス
    /// </summary>
    [Required]
    public required string Email { get; set; }

    /// <summary>
    /// アイデンティティアイコンURL（表示用）
    /// AvatarTypeに応じて適切なURL（Gravatar/UserAvatar/AutoGenerated）が設定される
    /// </summary>
    [Required]
    public required string IdentityIconUrl { get; set; }

    /// <summary>
    /// ワークスペース内での役割
    /// </summary>
    public string? WorkspaceRole { get; set; }

    /// <summary>
    /// アクティブフラグ
    /// </summary>
    public bool IsActive { get; set; }
}
